#include "geometry_1d.h"
#include <iostream>


////////////////////////////////////////////////////////////////////////////////////////
// Start of Line
////////////////////////////////////////////////////////////////////////////////////////

Line::Line():leftLen(-1.5), rightLen(1.5) {}

bool Line::operator()(double x, double y, double z) const {
	return (x <= rightLen && x >= -leftLen);
}

void Line::getBoundingBox(double& xmin, double& xmax, double& ymin, double& ymax, double& zmin, double& zmax) {
	xmin = leftLen;
	xmax = rightLen;
	ymin = 0;
	ymax = 0;
	zmin = 0;
	zmax = 0;
}

